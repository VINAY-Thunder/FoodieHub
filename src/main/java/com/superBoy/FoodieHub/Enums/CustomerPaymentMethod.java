package com.superBoy.FoodieHub.Enums;

public enum CustomerPaymentMethod {
	    UPI,           // Unified Payments Interface, GPay, PhonePe
	    CARD,          // Credit, Debit, and Prepaid Cards
	    NETBANKING,    // Standard Internet Banking across banks
	    WALLET,        // Amazon Pay, MobiKwik, Freecharge
	    EMI,           // Equated Monthly Installments on cards
	    PAYLATER,      // Cardless EMI and PayLater services
	    BANK_TRANSFER, // Virtual accounts: NEFT, RTGS, IMPS
	    CASH           // Internal offline or COD tracking
}
